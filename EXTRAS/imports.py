import math
#ceil joga pra cima o numero
print(math.ceil(5.0000000000000)) #no caso do 0, ele corta os 0 e deicxa só o 5
print(math.ceil(5.0000000000001)) # ja vai pra 6 o resultado
print(math.ceil(5.9999999999999)) # resultado 6
# o floor() joga pra baixo o valor
print(math.floor(5.99999999)) #resultado será 5
